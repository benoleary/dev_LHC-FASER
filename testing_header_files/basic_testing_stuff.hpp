#ifndef BASIC_TESTING_STUFF_HPP_
#define BASIC_TESTING_STUFF_HPP_

#include "../CppSLHA/CppSLHA.hpp"
#include "../LHC-FASER_global_stuff.hpp"

namespace LHC_FASER
{
  class basicStuffTesting
  {
  public:
    basicStuffTesting( std::string const slhaFileName ) :
      slhaData( slhaFileName ),
      bluhString( "bluh" ),
      testReadier()
    {
      slhaData.read_file();
    }
    ~basicStuffTesting()
    {
      // does nothing.
    }

    CppSLHA::CppSLHA2*
    getSlha()
    {
      return &slhaData;
    }

    std::string const* const
    getBluh()
    {
      return &bluhString;
    }

    readierForNewPoint*
    getReadier()
    {
      return &testReadier;
    }


  protected:
    CppSLHA::CppSLHA2 slhaData;
    std::string bluhString;
    readierForNewPoint testReadier;
  };

}  // end of LHC_FASER namespace

#endif /* BASIC_TESTING_STUFF_HPP_ */
